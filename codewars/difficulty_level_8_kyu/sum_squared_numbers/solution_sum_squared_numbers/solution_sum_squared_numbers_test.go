package solution_sum_squared_numbers

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestSumSquaredNumbers(t *testing.T) {
	assert.Equal(t, SumSquaredNumbers([]int{1, 2}), 5)
	assert.Equal(t, SumSquaredNumbers([]int{0, 3, 4, 5}), 50)
	assert.Equal(t, SumSquaredNumbers([]int{}), 0)
	assert.Equal(t, SumSquaredNumbers([]int{-1, -2}), 5)
	assert.Equal(t, SumSquaredNumbers([]int{-1, 0, 1}), 2)
}

